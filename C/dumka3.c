double norm(const unsigned int neqn, const double* x) {
	double norm = 0.;
	unsigned int i;
	for (i = 0; i < neqn; i++) {
		norm = norm + x[i] * x[i];
	};
	return sqrt(norm);
};
/* isStepAccepted - return true if step can be accepted, and at the same time this function
*                  calculates and return h_new
*
*/
bool  isStepAccepted(const unsigned int neqn, const double* z, double* h_new,
	double* err_n, double* h_n, long stepId) {
	bool isnotreject = false;
	double eps1 = 0.e0;
	int i;
	double eps;
	double fracmin;
	double frac;
	double fracmax;
	double h_old;
	for (i = 0; i < neqn; i++) {
		eps1 = eps1 + z[i] * z[i];
	};
	eps = sqrt(eps1 / ((double)neqn));
	fracmin = 0.1e0;
	if (eps == 0.e0) {
		eps = 1.e-14;
	}
	frac = pow(1.e0 / eps, 1.e0 / 3.e0);
	if (eps <= 1.e0) {
		if ((*err_n) > 0.e0 && (*h_n) > 0.e0) {
			double frac2 = pow(*err_n, (1.e0 / 3.e0)) * frac * frac * ((*h_new) / (*h_n));
			frac = min(frac, frac2);
		}
		isnotreject = true;
		fracmax = 2.e0;
		frac = min(fracmax, max(fracmin, 0.8e0 * frac));
		h_old = *h_new;
		(*h_new) = frac * (*h_new);
		*h_n = h_old;
		*err_n = eps;
	}
	else {
		isnotreject = false;
		fracmax = 1.e0;
		frac = 0.8e0 * min(fracmax, max(fracmin, 0.8e0 * frac));
		if (stepId == 0) {
			*h_new = fracmin * (*h_new);
		}
		else {
			*h_new = frac * (*h_new);
		}
		// printf("err= %e time= %e degree= %d new step= %e old step= %e  \n", eps, t, *n_pol_degree, *h_new, h_n);
	}
	return isnotreject;
};


/* eigenvalue - calculates eigen values of the right hand side
*              The used algorithm is a slight modification of the algorithm
*              used in ROCK and RKC
*/
double eigenvalue(const unsigned int neqn, const double* y, double* fy, double t,
	const FcnEqDiff f,
	double* v, double* result,
	double* lambdaOld, unsigned* numOfRHSEvaluations, double minimumValue)
{
	unsigned int maxit = 30, i = 0;
	double tol = 0.01;
	int idx = 0;
	double radius = 1.e-10;
	double lambda = *lambdaOld;
	double norminv;
	//double nrmY = norm(neqn, y);
	double nrmV = norm(neqn, v);
	//printf( "Compute eigenvalue ...\n" ); 
	do {
		idx++;
		// all vectors should be ||y-v|| < radius, because problem can be non-linear
		if (nrmV < minimumValue)
		{
			// change initial vector and restart - if restarted check that
			// initial vector has not been used already
			for (i = 0; i < neqn; i++)
			{
				v[i] = 0.01 * (rand() % 100);
			};
			nrmV = norm(neqn, v);
			if (nrmV < minimumValue) {
				printf("Error occur: your GetNonZeroElement function should return a value with strictly positive norm, but it returns 0!\n");
				printf("Fix GetNonZeroElement function, please. \n");
				return *lambdaOld;
			};
		};
		norminv = radius / nrmV;
		for (i = 0; i < neqn; i++) {
			v[i] = y[i] + norminv * v[i];
		};

		f(&neqn, &t, v, result);
		(*numOfRHSEvaluations)++;
		// approximation of eigenvector
		for (i = 0; i < neqn; i++) {
			v[i] = result[i] - fy[i];
		}
		// approximation of eigenvalue
		nrmV = norm(neqn, v);
		*lambdaOld = lambda;
		lambda = nrmV / radius;
		//printf( "Iteration = %d eigenvalue = %e \n" , idx, lambda );
	} while ((abs((double)(lambda - *lambdaOld)) > (abs(lambda) * tol)) && (idx <= maxit));
	*lambdaOld = lambda;
	return 1.2 * lambda;
};


