/* ------- ABSTRACT -----------------------------------
* DUMKA3 - integrates initial value problems for systems of first
* order ordinary differential equations y'=f(y,t).
* It is based on a family of explicit Runge-Kutta-Chebyshev formulas
* of order three.
* It uses optimal third order accuracy stability polynomials
* with the largest stability region along the negative real axis.
*
*
* The algorithm dumka3 and dumka4 is described in the article:
* Medovikov A.A. High order explicit methods for parabolic
*                equations. BIT, V38,No2,pp.372-390,1998
*
* Many thanks Prof. Lebedev V.I., Prof. Wanner G. and Prof. Hairer E.
*
* In order to call the program dumka3:
*
* void dumka3(long neqn, double* time, const double tend, const double h0,
*			const double atol, const double rtol,
*			void (f(long neqn, double* y, double t, double* result)),
*			double (cour(double* y, double t, void (f(long neqn, double* y, double t, double* result)))),
*			double* y,double* z0,double* z1,double* z2,
*			double* z3,double* oldEigenVector,
*			bool isComputeEigenValue) {
*
*
* You should define:
* neqn - number of equations
* t    - initial time
* tend - end time
* h0 - initial step size
* atol - absolute tolerance
* rtol - relative tolerance
* y - initial value, which will be
*     substituted by results of computations at t=tend
* z0,z1,z2, z3, oldEigenVector - working arrays of the length neqn
* isComputeEigenValue - true/false (true - if you would like
*     the program try to compute spectral radius
*     (which is required for the function cour)
*     false - if you want to define cour by yourself
*
*  you also have to define two external functions in order to
*  compute right hand side:
*  f(long neqn, double* y, double t, double* result)
*  and external function:
*  cour(double* y, double t, void (f(long neqn, double* y, double t, double* result)))
*  which caompute maximal possible step size of explicit Euler method for your
*  right hand side
*  cour=2/rho, where rho is a spectral radius
*  this function will not be used is isComputeEigenValue = false.
*
*  For any questions/suggestions/comments, please send e-mail to: amedovik@math.tulane.edu
*
*/
